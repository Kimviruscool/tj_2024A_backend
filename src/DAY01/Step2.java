package DAY01; //현재 클래스의파일이 위치한 패키지 폴더

public class Step2 { //class start //모든 코드는 class안에서만 작성한다.

    //실행코드는 main+enter 눌러서 main함수 생성 //코드 읽어주는 역할(스레드) 포함
    public static void main(String[] args) { //main start

        //[p.45]
        int level; //정수(소수점없는 숫자)형 변수 level이름으로 생성
        level = 10; //level 이라는 변수에 10을 대입 (= 대입 연산자)
        System.out.println(level); //level 변수 출력

        //[p 46]
        int level2 = 10; //정수형 level2이름으로 변수 선언하며 10대입
        System.out.println(level2);



    } //main end

} //class end
